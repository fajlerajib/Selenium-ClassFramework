public class Practise_Even_OddNumber {

	public static void main(String[] args) {

		// Even and Odd number

		for (int i = 0; i < 200; i++) {

			// System.out.println(i);

			if (i % 2 == 0) {
				System.out.println("  ---------------------  ");
				System.out.println("This is a Even Number :" + i + ",");

			} else {
				System.out.println("This is a ODD Number  :" + i + ",");

			}

		}

	}
}
